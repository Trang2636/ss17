let num1 = Math.abs(-5.5);
let num2 = Math.abs(10);
console.log(num1); // Kết quả là 5.5, hàm trả về giá trị tuyệt đối, bỏ dấu -
console.log(num2); // Kết quả là 10, vì 10 đã dương

let num3 = Math.ceil(4.4);
let num4 = Math.ceil(5.8);
console.log(num3); // Kết quả là 5, hàm làm tròn lên tới số nguyên gần nhất.
console.log(num4); // Kết quả là 6, hàm làm tròn lên tới số nguyên gần nhất.

let num5 = Math.floor(5.7);
let num6 = Math.floor(6.1);
console.log(num5); // Kết quả là 5, hàm làm tròn xuống tới số nguyên gần nhất.  
console.log(num6); // Kết quả là 6, hàm làm tròn xuống tới số nguyên gần nhất.  

let num7 = Math.round(10.1);
let num8 = Math.round(20.6);
console.log(num7); // Kết quả là 10, hàm làm tròn về phía số nguyên gần nhất (nhỏ hơn).
console.log(num8); // Kết quả là 21, hàm làm tròn về phía số nguyên gần nhất (lớn hơn).